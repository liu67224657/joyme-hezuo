<h2><span class="mw-headline" id=".E6.94.BB.E7.95.A5.E8.A6.81.E7.82.B9">攻略要点</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/cq/index.php?title=%E7%81%B5%E9%AD%82%E8%A6%81%E5%A1%9E%E6%94%BB%E7%95%A5%E6%A6%82%E8%A7%88&amp;veaction=edit&amp;vesection=4" title="编辑小节：攻略要点" class="mw-editsection-visualeditor">编辑此段落</a><span class="mw-editsection-divider"> | </span><a href="/cq/index.php?title=%E7%81%B5%E9%AD%82%E8%A6%81%E5%A1%9E%E6%94%BB%E7%95%A5%E6%A6%82%E8%A7%88&amp;action=edit&amp;section=4" title="编辑小节：攻略要点">源代码</a><span class="mw-editsection-bracket">]</span></span></h2>
<p><big><b>■尽可能多的培养勇士</b></big>
</p><p>灵魂要塞中，每位勇士每天只能上场1次。如果每天都要刷完一轮（7关）的话，至少需要<font color="#FF6EB4">21名</font>可用的勇士，对玩家的BOX深度是一个不小的考验。
</p><p><br />
<big><b>■回血英雄重在稳定</b></big>
</p><p>因为不可能完全凭借输出秒掉对手，所以队伍中的回血担当至关重要，至少需要<a href="/cq/%E5%85%8B%E9%B2%81%E8%B5%9B%E5%BE%B7%E6%88%98%E8%AE%B05%E2%98%85%E5%88%97%E8%A1%A8" title="克鲁赛德战记5★列表">5★</a>以上的<a href="/cq/%E7%A5%AD%E5%8F%B8" title="祭司">祭司</a>，需要提前培养5~6位这样的角色。
</p><p><br />
<big><b>■根据关卡难易度调整队伍</b></big>
</p><p>当然，关卡的难度是逐渐递增的，玩家可以根据给出的BOSS信息来及时调整队伍的构成（特别是在前期的关卡中）。除了<a href="/cq/%E5%85%8B%E9%B2%81%E8%B5%9B%E5%BE%B7%E6%88%98%E8%AE%B06%E2%98%85%E5%88%97%E8%A1%A8" title="克鲁赛德战记6★列表">6★</a>之外，前期也可以投入<a href="/cq/%E5%85%8B%E9%B2%81%E8%B5%9B%E5%BE%B7%E6%88%98%E8%AE%B04%E2%98%85%E5%88%97%E8%A1%A8" title="克鲁赛德战记4★列表">4★</a>和<a href="/cq/%E5%85%8B%E9%B2%81%E8%B5%9B%E5%BE%B7%E6%88%98%E8%AE%B05%E2%98%85%E5%88%97%E8%A1%A8" title="克鲁赛德战记5★列表">5★</a>的英雄。
</p><p><br />
<big><b>■需要注意BOSS的专武？</b></big>
</p><p><font color="#FF6EB4">各关卡中的BOSS都会带专武。</font>但在实际战斗中不会发挥出专武的实力，所以不必过分担心，但如果是<a href="/cq/%E6%9C%80%E5%88%9D%E7%9A%84%E5%B8%95%E6%8B%89%E4%B8%81%E7%BD%97%E5%85%B0" title="最初的帕拉丁罗兰">6★罗兰</a>这样的强力英雄，还是需要注意一下的。
</p><p><br />
<big><b>■力保通关前半程关卡</b></big>
</p><p>在刚刚解锁灵魂要塞的一段时间之内，可能只能打到前面的一些关卡就会卡住，这是很正常的。这时候得到的<font color="#FF6EB4"><b>水晶粉末</b></font>也是精淬武器的重要材料，可以重复刷前期的关卡，<font color="#FF6EB4">收集材料打造专武</font>，提升队伍战斗力。
</p><p><br />
</p>
<h2><span class="mw-headline" id=".E5.85.B3.E4.BA.8E.E7.AC.AC.E5.85.AB.E5.B1.82BOSS.E7.B4.A2.E5.A1.94.E5.B0.94">关于第八层BOSS索塔尔</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/cq/index.php?title=%E7%81%B5%E9%AD%82%E8%A6%81%E5%A1%9E%E6%94%BB%E7%95%A5%E6%A6%82%E8%A7%88&amp;veaction=edit&amp;vesection=5" title="编辑小节：关于第八层BOSS索塔尔" class="mw-editsection-visualeditor">编辑此段落</a><span class="mw-editsection-divider"> | </span><a href="/cq/index.php?title=%E7%81%B5%E9%AD%82%E8%A6%81%E5%A1%9E%E6%94%BB%E7%95%A5%E6%A6%82%E8%A7%88&amp;action=edit&amp;section=5" title="编辑小节：关于第八层BOSS索塔尔">源代码</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>9月23日更新的灵魂要塞第八层是目前公认的高难度关卡，一开场就要直面BOSS索塔尔。通关后可获得丰富的铁。
</p><p>BOSS索塔尔的技能非常强力，而且很灵活。 <font color="#FF6EB4">体力突破下限后会发动狂暴效果</font>，除了SP技能之外，普通攻击也具有相当的威胁性。
</p><p><br />
<big><b>■攻略要点</b></big>
