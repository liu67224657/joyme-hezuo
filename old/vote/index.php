<script>
var alertbar={
    'int':function(){
        alertbar.show();
        alertbar.hide();
    },
    'show':function(){
        $('.subBtn').on('click',function(){
            var str1 = "";
            var str2 = "";
            var btn1 = $("input[name='btn1']").filter(':checked').val()
            var btn2 = $("input[name='btn2']").filter(':checked').val()
            var btn3 = $("input[name='btn3']").filter(':checked').val()
            var flag = "joymevote";

            if(btn1 && btn2 && btn3){
                if(btn1 == btn2 || btn1 == btn3 || btn2 == btn3){
                    str1 = "投票失败"
                    str2 = "不能重复选择同一作品！"
                }else{
                    jQuery.ajax({
                        "url": "vote.php",
                        "type": "post",
                        "data": {"btn1":btn1,"btn2":btn2,"btn3":btn3,"flag":flag},
                        "success": function(msg) {
                            if(msg=="NO"){
                                str1 = "投票失败！"
                                str2 = "您已经投过票了"
                            }else if(msg=="DANGER"){
                                str1 = "投票失败！"
                                str2 = "不允许非法投票"
                            }else{
                                str1 = "投票成功！"
                                str2 = "感谢您对本次活动支持"
                            }
                            $("#result").text(str1);
                            $("#result2").text(str2);
                            $('.alert').css('display','block');
                        }
                    })
                }
            }else{
                str1 = "投票失败"
                str2 = "您还有作品未选中！"
            }
            $("#result").text(str1);
            $("#result2").text(str2);
            $('.alert').css('display','block');
        });
    },
    'hide':function(){
        $('.void').on('click',function(){
            $('.alert').css('display','none');  
        })
    }
}
alertbar.int();
