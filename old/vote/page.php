                     <span>速度依赖于优秀的团队、优秀的个体、优秀的管理。在大面上快速的同时，也要注意适度。过高的估值，曾为tumblr带来了快速的流量增长和巨大的财富，也造成了一段时间的价值和流量跳水。但其团队始终围绕真正目标用户的需求开发、推广，用户的自然使用和自然支持，使之形成了稳定的，可见的独特性。</span>
