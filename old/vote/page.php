                     <span>速度和远见，这是我在十集看完，思考最多的话题。</span>
                     <span>速度，不仅是现象，也是互联网创立的初心。由最初的，让信息更快的传输，让控制更快地延伸，才有互联网。今天，信息和控制的结合，延展到几乎全部的生活场景，得以缔造时代。门槛被降低，催生行动力，在技术积累、知识积累下，一切得以快速实现。</span>
                     <span>速度不见得再是动力强化，在互联网的背景下，你可以在这个时空里，直接缩短两点之间的距离，甚至缩短一个点到一切点的距离。雅虎，一个普通的目录网站，由于其简单明了的解决方案，对超链接方案最行之有效的利用。用如此之短的时间，帮助巨量用户有了第一张互联网的地图。而谷歌，则缩短了一切主动查找的动作距离。

</span>
                     <span>速度不见得再靠自己跑得快，LAMP、GITHUB、LINUX、云服务、搜索标签技术、平台化登录，开源减少了发明轮子的时间，云服务、标签优化、开放登录提供了细分服务，新的互联网开发者，可以把全部的精力集中在创意和优化上。</span>
